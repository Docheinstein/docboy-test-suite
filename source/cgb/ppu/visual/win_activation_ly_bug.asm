; Window should not activate because of LY bug on CGB.

EntryPoint:
    DisablePPU

    ; Set WY
    ld a, 0
    ldh [rWY], a

    ; Set WX
    ld a, 0
    ldh [rWX], a

    ; Set SCX=0
    ld a, $00
    ldh [rSCX], a

    ; Enable VBlank interrupt
    xor a
    ldh [rIF], a

    ld a, $01
    ldh [rIE], a

    ; Write BG palettes
    ;               red+green             red+blue               green+blue             grey
    SetBGP 0, %11111111, %00000011, %00011111, %01111100, %11100000, %01111111, %11100111, %00011100

    ; Write OBJ palettes
    ;                 red                  green                   blue                black
    SetOBJP 0, %00011111, %00000000, %11100000, %00000011, %00000000, %01111100, %00000000, %00000000

    ; Reset VRAM
    Memset $8000, $00, $2000

    ; Background tile map
    Memcpy $9800, BackgroundTileMapData, BackgroundTileMapDataEnd - BackgroundTileMapData

    ; Copy ASCII tilemap to VRAM $9000 area.
    Memcpy $8000, AsciiTilemap, AsciiTilemapEnd - AsciiTilemap

    EnablePPU

    jp FrameEnd

Loop:
    ; Win off
    ld a, LCDCF_ON | LCDCF_BGON | LCDCF_WINOFF | LCDCF_BG9800 | LCDCF_WIN9800 | LCDCF_BG8000
	ldh [rLCDC], a

    Wait (120 + 10) * 114

    ; Win on
    ld a, LCDCF_ON | LCDCF_BGON | LCDCF_WINON | LCDCF_BG9800 | LCDCF_WIN9800 | LCDCF_BG8000
	ldh [rLCDC], a

FrameEnd:
	ei

    halt

    jp Loop


