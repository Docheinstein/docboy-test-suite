INCLUDE "all.inc"

; Changing WX in CGB might trigger a bug that depending on the fetcher
; phase make it either read an old tile data or do not read the tile number.

EntryPoint:
    DisablePPU

    ; Set WY
    ld a, 8
    ldh [rWY], a

    ; Set SCX=0
    ld a, $00
    ldh [rSCX], a

    ; Enable VBlank interrupt
    xor a
    ldh [rIF], a

    ld a, $01
    ldh [rIE], a

    ; Write BG palettes
    ;               red+green             red+blue               green+blue             grey
    SetBGP 0, %11111111, %00000011, %00011111, %01111100, %11100000, %01111111, %11100111, %00011100

    ; Write OBJ palettes
    ;                 red                  green                   blue                black
    SetOBJP 0, %00011111, %00000000, %11100000, %00000011, %00000000, %01111100, %00000000, %00000000

    ; Reset VRAM
    Memset $8000, $00, $2000

    ; Background tile map
    Memcpy $9800, BackgroundTileMapData, BackgroundTileMapDataEnd - BackgroundTileMapData

    ; Copy ASCII tilemap to VRAM $9000 area.
    Memcpy $8000, AsciiTilemap, AsciiTilemapEnd - AsciiTilemap

    ld a, LCDCF_ON | LCDCF_BGON | LCDCF_WINON | LCDCF_BG9C00 | LCDCF_WIN9800 | LCDCF_BG8000 ; Reset
	ldh [rLCDC], a

    ei

Loop:
    Wait 8 * 114

DEF NUM_NOPS EQU 17

FOR I, 128
    ld a, 6
    ldh [rWX], a

    Nops NUM_NOPS

    ld a, I
    ldh [rWX], a

    Nops (114 - NUM_NOPS - 10)
ENDR

    halt

    Wait 114 * 10 - 18

    jp Loop

    jp TestFail


